   .org 0x0
   .set noat
   .set noreorder
   .set nomacro
   .global _start
_start:
   ori  $3,$0,0xeeff     # $3 = 0x0000eeff
   sb   $3,0x3($0)       # 向RAM地址0x3处存储0xff,[0x3] = 0xff
   srl  $3,$3,8          # 逻辑右移8位,$3 = 0x000000ee
   sb   $3,0x2($0)       # 向RAM地址0x2处存储0xee,[0x2] = 0xee
   ori  $3,$0,0xccdd     # $3 = 0x0000ccdd
   sb   $3,0x1($0)       # 向RAM地址0x1处存储0xdd,[0x1] = 0xdd
   srl  $3,$3,8          # 逻辑右移8位,$3 = 0x000000cc
   sb   $3,0x0($0)       # 向RAM地址0x0处存储0xcc,[0x0] = 0xcc
   lb   $1,0x3($0)       # 加载0x3处的字节并作符号扩展,$1 = 0xffffffff
   lbu  $1,0x2($0)       # 加载0x2处的字节并作无符号扩展,$1 = 0x000000ee
   nop

   ori  $3,$0,0xaabb     # $3 = 0x0000aabb
   sh   $3,0x4($0)       # 向RAM地址0x4处存储0xaabb
                         # [0x4] = 0xaa, [0x5] = 0xbb
   lhu  $1,0x4($0)       # 加载0x4,$1 = 0x0000aabb
   lh   $1,0x4($0)       # 加载0x4,$1 = 0xffffaabb
 
   ori  $3,$0,0x8899     # $3 = 0x00008899
   sh   $3,0x6($0)       # 向RAM地址0x6处存储0x8899
                         # [0x6] = 0x88, [0x7] = 0x99
   lh   $1,0x6($0)       # 加载0x6,$1 = 0xffff8899
   lhu  $1,0x6($0)       # 加载0x6,$1 = 0x00008899

   ori  $3,$0,0x4455     # $3 = 0x00004455
   sll  $3,$3,0x10       # $3 = 0x44550000
   ori  $3,$3,0x6677     # $3 = 0x44556677
   sw   $3,0x8($0)       # 向RAM地址0x8处存储0x44556677
                         # [0x8] = 0x44, [0x9]= 0x55, [0xa]= 0x66, [0xb] = 0x77
   lw   $1,0x8($0)       # 加载0x8,$1 = 0x44556677

   lwl  $1, 0x5($0)      # 非对齐加载,$1 = 0xbb889977
   lwr  $1, 0x8($0)      # 非对齐加载,$1 = 0xbb889944

   nop
   swr  $1, 0x2($0)      # [0x0] = 0x88, [0x1] = 0x99, [0x2]= 0x44, [0x3] = 0xff
   swl  $1, 0x7($0)      # [0x4] = 0xaa, [0x5] = 0xbb, [0x6] = 0x88, [0x7] = 0xbb   #[0x7] = 0x44

   lw   $1, 0x0($0)      # $1 = 0x889944ff
   lw   $1, 0x4($0)      # $1 = 0xaabb88bb
    
_loop:
   j _loop
   nop
